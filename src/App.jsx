// ─── Flow Indicator ───────────────────────────────────────────────────────────
function FlowIndicator({ step }) {
  const steps = [
    { num: 1, label: "Input" },
    { num: 2, label: "Gemini Analysis" },
    { num: 3, label: "Explanation" },
    { num: 4, label: "Map" },
  ];

  const getState = (n) => {
    if (step === 0) return n === 1 ? "active" : "idle";
    if (step === 1) return n <= 1 ? "done" : n === 2 ? "active" : "idle";
    if (step === 2) return "done";
    return "idle";
  };

  return (
    <div className="flow-bar">
      {steps.map((s, i) => {
        const state = getState(s.num);
        return (
          <div key={s.num} style={{ display: "flex", alignItems: "center", flex: i < steps.length - 1 ? "1" : "none" }}>
            <div className={`flow-step ${state}`}>
              <div className="flow-step-num">
                {state === "done" ? "✓" : s.num}
              </div>
              <span style={{ whiteSpace: "nowrap" }}>{s.label}</span>
            </div>
            {i < steps.length - 1 && <div className="flow-arrow" />}
          </div>
        );
      })}
    </div>
  );
}

