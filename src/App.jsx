// ─── Main App ─────────────────────────────────────────────────────────────────
export default function App() {
  const [selectedScenario, setSelectedScenario] = useState(null);
  const [analysis, setAnalysis] = useState(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);
  const [insufficientData, setInsufficientData] = useState(false);
  const [customSignal, setCustomSignal] = useState("");
  const [customLoading, setCustomLoading] = useState(false);
  const [inputError, setInputError] = useState(null);
  const [time, setTime] = useState(new Date().toLocaleTimeString());

  useEffect(() => {
    const t = setInterval(() => setTime(new Date().toLocaleTimeString()), 1000);
    return () => clearTimeout(t);
  }, []);

  const flowStep = loading || customLoading ? 1 : analysis || error || insufficientData ? 2 : 0;

  // ── Demo state trigger ──
  const handleDemoState = (state) => {
    const baseScenario = disasterScenarios[0]; // Chennai Flood 2026
    setSelectedScenario(baseScenario);
    setAnalysis(null);
    setError(null);
    setInsufficientData(false);
    setInputError(null);

    if (state === "success") {
      // Show real mock Gemini response
      setAnalysis(demoResponses[1]);
    } else if (state === "insufficient") {
      // Simulate ambiguous data — no threat_level returned
      setInsufficientData(true);
    } else if (state === "failed") {
      // Simulate API/network failure
      setError("Gemini service unavailable — check your API key in the .env file.");
    }
  };

  // ── Live Gemini analyze ──
  const handleAnalyze = async (scenario) => {
    setSelectedScenario(scenario);
    setLoading(true);
    setAnalysis(null);
    setError(null);
    setInsufficientData(false);
    setInputError(null);
    try {
      const result = await analyzeDisasterSignals(scenario.signals, scenario.name);
      if (!result?.threat_level) {
        setInsufficientData(true);
      } else {
        setAnalysis(result);
      }
    } catch (err) {
      console.error("Gemini error:", err);
      setError("Gemini service unavailable — check your API key in the .env file.");
    } finally {
      setLoading(false);
    }
  };

  // ── Custom Signal ──
  const handleCustomSignal = async () => {
    if (!customSignal.trim() && !selectedScenario) {
      setInputError("Please describe a crisis signal or select an incident feed first.");
      return;
    }
    if (!customSignal.trim()) {
      setInputError("Please enter a signal description before analyzing.");
      return;
    }
    setInputError(null);
    setCustomLoading(true);
    setError(null);
    setInsufficientData(false);
    try {
      const result = await analyzeCustomSignal(customSignal, selectedScenario?.signals ?? []);
      if (!result?.threat_level) {
        setInsufficientData(true);
      } else {
        setAnalysis(result);
        setCustomSignal("");
      }
    } catch (err) {
      console.error("Custom signal error:", err);
      setError("Gemini service unavailable — please try again.");
    } finally {
      setCustomLoading(false);
    }
  };

  const isLoading = loading || customLoading;

  return (
    <div className="grid-bg" style={{ minHeight: "100vh", background: "var(--bg-primary)", display: "flex", flexDirection: "column" }}>

      {/* ── Header ── */}
      <header className="app-header">
        <div style={{ display: "flex", alignItems: "center", gap: "0.9rem" }}>
          <div style={{ width: "38px", height: "38px", border: "2px solid var(--cyan)", borderRadius: "9px", display: "flex", alignItems: "center", justifyContent: "center", fontSize: "1.15rem", boxShadow: "0 0 14px rgba(34,211,238,0.35)", flexShrink: 0 }}>⬡</div>
          <div>
            <div style={{ fontSize: "1.25rem", fontWeight: 900, color: "var(--cyan)", letterSpacing: "0.1em", lineHeight: 1.1 }}>INTELMAP</div>
            <div style={{ fontSize: "0.58rem", color: "var(--text-muted)", letterSpacing: "0.08em" }}>
              Explainable, Location-Aware Crisis Intelligence
            </div>
          </div>
        </div>
        <div style={{ flex: 1, margin: "0 1.5rem", overflow: "hidden" }}><Ticker /></div>
        <div style={{ display: "flex", alignItems: "center", gap: "1.25rem", flexShrink: 0 }}>
          <div style={{ textAlign: "right" }}>
            <div className="mono" style={{ fontSize: "0.88rem", color: "var(--text-primary)", fontWeight: 600 }}>{time}</div>
            <div style={{ fontSize: "0.6rem", color: "var(--text-muted)", letterSpacing: "0.08em" }}>UTC+5:30</div>
          </div>
          <div style={{ display: "flex", alignItems: "center", gap: "6px", padding: "4px 10px", background: "rgba(34,197,94,0.1)", border: "1px solid rgba(34,197,94,0.3)", borderRadius: "20px" }}>
            <span style={{ width: "6px", height: "6px", borderRadius: "50%", background: "var(--green)", display: "inline-block" }} className="animate-pulse-slow" />
            <span style={{ fontSize: "0.6rem", fontWeight: 700, color: "var(--green)", letterSpacing: "0.1em" }}>GEMINI ONLINE</span>
          </div>
        </div>
      </header>

      {/* ── Demo State Bar ── */}
      <DemoBar onTrigger={handleDemoState} />

      {/* ── Body ── */}
      <div className="app-body" style={{ flex: 1 }}>

        {/* LEFT PANEL */}
        <aside className="panel-left">
          <div style={{ padding: "1rem", borderBottom: "1px solid var(--border)" }}>
            <div className="label" style={{ marginBottom: "0.5rem" }}>Select Incident Feed</div>
            <div style={{ fontSize: "0.68rem", color: "var(--text-muted)", lineHeight: 1.6 }}>
              Choose a disaster scenario to begin Gemini analysis.
            </div>
          </div>

          <div style={{ padding: "0.75rem", display: "flex", flexDirection: "column", gap: "0.5rem", flex: 1 }}>
            {disasterScenarios.map((s) => {
              const active = selectedScenario?.id === s.id;
              return (
                <button key={s.id} onClick={() => handleAnalyze(s)} disabled={isLoading}
                  className={`scenario-btn ${active ? "active" : ""}`}
                  style={{ border: `1px solid ${active ? s.color : "var(--border)"}` }}>
                  <div style={{ display: "flex", justifyContent: "space-between", alignItems: "flex-start" }}>
                    <div style={{ fontSize: "1.25rem", marginBottom: "4px" }}>{s.emoji}</div>
                    {active && (
                      <span style={{ fontSize: "0.55rem", fontWeight: 700, letterSpacing: "0.1em", color: s.color, background: `${s.color}18`, border: `1px solid ${s.color}50`, borderRadius: "4px", padding: "1px 6px" }}>ACTIVE</span>
                    )}
                  </div>
                  <div style={{ fontWeight: 700, fontSize: "0.8rem", color: active ? s.color : "var(--text-primary)" }}>{s.name}</div>
                  <div style={{ fontSize: "0.67rem", color: "var(--text-muted)", marginTop: "2px" }}>{s.location}</div>
                  <div style={{ fontSize: "0.62rem", color: "var(--text-muted)", marginTop: "4px" }}>
                    {s.signals.length} signals · {s.lat.toFixed(2)}°N
                  </div>
                </button>
              );
            })}
          </div>

          {/* Custom Signal */}
          <div style={{ padding: "1rem", borderTop: "1px solid var(--border)" }}>
            <div style={{ display: "flex", alignItems: "center", gap: "0.5rem", marginBottom: "0.6rem" }}>
              <div className="label">Inject Custom Signal</div>
              <GeminiTag label="via Gemini" />
            </div>
            <textarea
              className="signal-textarea"
              value={customSignal}
              onChange={(e) => { setCustomSignal(e.target.value); setInputError(null); }}
              placeholder={`e.g. "Dam breach reported near Sector 9 — 800 families at risk"`}
              rows={3}
              onKeyDown={(e) => { if (e.key === "Enter" && e.ctrlKey) handleCustomSignal(); }}
            />
            {inputError && (
              <div style={{ fontSize: "0.68rem", color: "var(--red)", marginTop: "0.4rem", lineHeight: 1.5 }}>⚠ {inputError}</div>
            )}
            <button className="btn-analyze" onClick={handleCustomSignal}
              disabled={customLoading || (!customSignal.trim() && !selectedScenario)}
              style={{ marginTop: "0.6rem" }}>
              {customLoading
                ? <><div className="spinner" style={{ width: "14px", height: "14px", borderWidth: "2px" }} /> Analyzing…</>
                : "⬡ Analyze with Gemini"}
            </button>
            <div style={{ fontSize: "0.58rem", color: "var(--text-muted)", textAlign: "center", marginTop: "0.4rem" }}>
              Ctrl + Enter to submit
            </div>
          </div>
        </aside>

        {/* CENTER PANEL */}
        <div className="panel-center">
          <FlowIndicator step={flowStep} />
          <div className="panel-center-scroll">

            {/* ── State: Analysis Failed ── */}
            {error && !isLoading && (
              <div className="error-banner animate-fadeIn">
                <span style={{ fontSize: "1.3rem", flexShrink: 0 }}>✕</span>
                <div>
                  <div style={{ fontWeight: 700, marginBottom: "4px", fontSize: "0.85rem" }}>Analysis Failed</div>
                  <div style={{ lineHeight: 1.65 }}>{error}</div>
                  <div style={{ marginTop: "0.5rem", fontSize: "0.7rem", color: "rgba(252,165,165,0.7)" }}>
                    Check your <code style={{ background: "rgba(239,68,68,0.1)", padding: "1px 4px", borderRadius: "3px" }}>.env</code> file and ensure <code style={{ background: "rgba(239,68,68,0.1)", padding: "1px 4px", borderRadius: "3px" }}>VITE_GEMINI_API_KEY</code> is set correctly.
                  </div>
                </div>
              </div>
            )}

            {/* ── State: Insufficient Data ── */}
            {insufficientData && !isLoading && (
              <div className="animate-fadeIn" style={{
                display: "flex", alignItems: "flex-start", gap: "0.75rem",
                padding: "1rem", background: "rgba(251,191,36,0.06)",
                border: "1px solid rgba(251,191,36,0.3)", borderRadius: "10px",
                color: "#fde68a", fontSize: "0.78rem", lineHeight: 1.6,
              }}>
                <span style={{ fontSize: "1.3rem", flexShrink: 0 }}>⚠</span>
                <div>
                  <div style={{ fontWeight: 700, marginBottom: "4px", fontSize: "0.85rem" }}>Insufficient Data to Classify</div>
                  <div>
                    The signals provided are too weak, incomplete, or ambiguous for Gemini to make a confident classification.
                  </div>
                  <div style={{ marginTop: "0.5rem", fontSize: "0.7rem", color: "rgba(253,230,138,0.7)" }}>
                    Try adding more specific signals — include location, severity level, or source type for better accuracy.
                  </div>
                </div>
              </div>
            )}

            {/* ── State: Loading ── */}
            {isLoading && <LoadingState signals={selectedScenario?.signals.length ?? 1} />}

            {/* ── State: Empty ── */}
            {!selectedScenario && !isLoading && !error && !insufficientData && <EmptyState />}

            {/* ── State: Full Analysis (Success) ── */}
            {analysis?.threat_level && !isLoading && (
              <GeminiResultPanel analysis={analysis} scenario={selectedScenario} />
            )}

          </div>
        </div>

        {/* RIGHT PANEL — Map */}
        <div className="panel-right">
          <div style={{ padding: "0.75rem 1rem", borderBottom: "1px solid var(--border)", display: "flex", alignItems: "center", gap: "0.5rem" }}>
            <div className="label" style={{ flex: 1 }}>Location Map</div>
            <span style={{ fontSize: "0.6rem", color: "var(--text-muted)" }}>Maps = context, not decoration</span>
          </div>
          <MapPanel scenario={selectedScenario} />
        </div>

      </div>
    </div>
  );
}
